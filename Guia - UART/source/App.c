/* Función que se llama 1 vez, al comienzo del programa */
void App_Init (void)
{
	gpioMode(PIN_LED_GREEN, OUTPUT);
	uartInit();
}

/* Función que se llama constantemente en un ciclo infinito */
void App_Run (void)
{
	
	if (((UART2->S1) & UART_S1_RDRF_MASK))
	{
		char ch=UART2->D;
		printf("%c",ch);
	}
}


